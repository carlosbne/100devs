//normal function
function addNums(num1, num2) {
   return num1 + num2;
}

console.log(addNums(5,4));

//arrow function
const addNums1 = (num1, num2) =>{
    return num1 + num2;
}

console.log(addNums1(5,3));

//outra forma
const addNums2 = (num1, num2) => num1 + num2;

//outra forma
const addNums3 = num1 => num1 + 5;

